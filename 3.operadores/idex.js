//  Operadores de asignación
//Un operador de asignación asigna un valor a su operando izquierdo basándose en el valor de su operando derecho.
// El operador de asignación simple es igual (=), que asigna el valor de su operando derecho a su operando izquierdo. 
//Es decir, x = y asigna el valor de y a x.

// ejemplo 

//Asignación	                                        x = y
//Asignación de adición		                            x = x + y
//Asignación de resta		                            x = x - y
//Asignación de multiplicación	                        x = x * y
//Asignación de división		                        x = x / y
//Asignación de residuo		                            x = x % y
//Asignación de exponenciación	                        x = x ** y
//Asignación de desplazamiento a la izquierda	        x = x << y
//Asignación de desplazamiento a la derecha		        x = x >> y
//Asignación de desplazamiento a la derecha sin signo	x = x >>> y
//Asignación AND bit a bit		                        x = x & y
//Asignación XOR bit a bit		                        x = x ^ y
//Asignación OR bit a bit		                        x = x | y
//Asignación AND lógico	                                x && (x = y)
//Asignación OR lógico	                                x || (x = y)
//Asignación de anulación lógica	                    x ??= y	x ?? (x = y)